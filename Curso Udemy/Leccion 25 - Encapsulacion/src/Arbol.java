


public class Arbol {
	
	// "public" indica acceso desde cualquier parte del proyecto
	// Esta se debe evitar a toda costa
	public int altura;
	
	// "protected" indica acceso desde el mismo paquete, la misma clase y, si la hay, desde una subclase
	// La encapsulacion del atributo debe indicarse en la clase padre
	protected String especie;

}
